const translations: Record<Language, TranslationDict> = { ar, en };

/**
 * Get a nested translation value using dot notation
 * e.g., t('auth.login') → translations[lang].auth.login
 */
export function t(key: string, lang: Language = 'ar'): string {
  const keys = key.split('.');
  let result: string | TranslationDict | undefined = translations[lang];

  for (const k of keys) {
    if (typeof result === 'object' && result !== null) {
      result = result[k];
    } else {
      return key; // fallback to key if not found
    }
  }

  if (typeof result === 'string') {
    return result;
  }

  return key; // fallback
}

/**
 * Get the text direction based on language
 */
export function getDirection(lang: Language): 'rtl' | 'ltr' {
  return lang === 'ar' ? 'rtl' : 'ltr';
}

/**
 * Format a number with locale-aware separators
 */
export function formatNumber(num: number, lang: Language = 'ar'): string {
  if (num >= 1000000) {
    const val = (num / 1000000).toFixed(1);
    return lang === 'ar' ? `${val}م` : `${val}M`;
  }
  if (num >= 1000) {
    const val = (num / 1000).toFixed(1);
    return lang === 'ar' ? `${val}ك` : `${val}K`;
  }
  return num.toString();
}

